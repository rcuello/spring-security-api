
-- Crear una base de datos llamada 'tecno_tienda'

CREATE DATABASE `tecno_tienda` /*!40100 DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci */ /*!80016 DEFAULT ENCRYPTION='N' */;


-- CREATE DATABASE :
	-- Es la instrucción SQL que se utiliza para crear una nueva base de datos en MySQL
-- /*!40100 ... */ 
	-- Es una directiva condicional que solo se ejecuta si la versión de MySQL es 4.0.100 o superior 
-- DEFAULT CHARACTER SET utf8mb4: 
	-- Establece el conjunto de caracteres predeterminado para la base de datos
-- utf8mb4: 
	-- Es un conjunto de caracteres que soporta todos los caracteres Unicode, incluyendo emojis y otros caracteres especiales
-- COLLATE utf8mb4_unicode_ci: 
	-- Establece la intercalación predeterminada para la base de datos
-- utf8mb4_unicode_ci: 
	-- Es una intercalación que se basa en el estándar Unicode y es adecuada para la mayoría de los casos de uso
-- DEFAULT ENCRYPTION='N': 
	-- Establece la encriptación predeterminada para la base de datos
	-- 'N' significa que la base de datos no está encriptada 
